using namespace std;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    // Read input but ignore, always output NO
    long long n, m, L, R, Sx, Sy, Lq, s;
    if (!(cin >> n >> m >> L >> R >> Sx >> Sy >> Lq >> s)) return 0;
    for (int i = 0; i < Lq; ++i) {
        long long x;
        cin >> x;
    }
    cout << "NO\n";
    return 0;
}